package tipos_datos;

public class ejercicio1 {

	public static void main(String[] args) {
		//Tipos de datos enteros
		int edad = 12;
		long edad1 = 40;



		//tipos de datos decimales
		float nota = 20.50f;

		//tipos de datos caracter
		char op = 'a';

		//tipos de datos booleanos
		boolean decision = true;

		//tipos de datos Integer
		Integer num = null;

		//datos String
		String palabra = "Hola mundo";

		//Constante
		final double PI = 3.141516;

		System.out.println("La edad de Jose es: " + edad);
		System.out.println("La edad de Carlos es: " + edad1);
		System.out.println("La nota de Carlos es: " + nota);
		System.out.println("La opcion elegida es: " + op);
		System.out.println("La decision es: " + decision);
		System.out.println("El valor del numero es: " + num);
		System.out.println("Las palabras son: " + palabra);

		System.out.println("El valor de PI es: " + PI);
	}

}
